package Chapter3;

public class TypeChar {
        public static void main (String[] args) {
            char chl, ch2;
            chl = 88; // код для Х
            ch2 = 'У';
            System.out.print(" chl и ch2 : ");
            System.out.println(chl + " " + ch2);
            // Вывод - chl и ch2 : Х У
        }
}
